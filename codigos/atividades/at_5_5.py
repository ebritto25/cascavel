lista = [58,14,2,69,87,68,45,21,0]

'''
    Função max retorna o maior elemento da lista e a função min retorna o menor
    elemento. É possível definir como as comparações são feitas passando uma função
    para o argumento "key"
'''
maior = max(lista)
menor = min(lista)

print(f'O maior item é {maior} e o menor é {menor}')
